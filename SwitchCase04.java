public class SwitchCase04 {
    public static void main(String[] args) {
        int num = 7;
        switch (num) {
            case 1:
                System.out.println("It is 5");
                break;

                case 7:
                System.out.println("It is 7");
                break;
        
            default:
                break;
        }
    }
}
